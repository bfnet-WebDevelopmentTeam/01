			令和5年10月15日(日)13時から15時まで（2時間）<br>
			登録講習修了者は、13時10分から15時まで（1時間50分）。<br>
			※ 当日は、受験に際しての注意事項の説明がありますので、12時30分までに自席に着席してください。<br>
