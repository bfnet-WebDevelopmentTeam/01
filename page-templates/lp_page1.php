			<p>宅建士は、不動産会社が公正な不動産取引をおこなうため、宅地建物取引業法により定められた国家資格です。正式名称を宅地建物取引士といいます。</p>
			<p>業務独占資格でもあり、不動産取引における重要事項の説明や、重要事項説明書への記名・押印、契約書への記名・押印ができるのは宅建士だけです。</p>
			<p>試験の合格率は約15％~17%です。初心者の方でも講座の受講などで合格に近付けます。</p>
			<p>受験資格はなく、どなたでも試験を受けられます。毎年、約20万人が受験している人気の資格です。</p>
			
			<h2 class="sub01" id="item8">令和5年度宅建スケジュール</h2>
			<p>令和5年度の試験は、下記のとおり実施します。令和5年6月2日に官報公告を行いました。</p>
			<p>インターネット申込み (令和5年7月19日(水)で終了しました)令和5年7月3日(月)9時30分から7月19日(水)21時59分まで<br>
				※インターネット申込みは原則として24時間利用可能です。<br>※インターネット申込みの試験案内の掲載は、令和5年6月26日(月)から行います。</p>
			<p>郵送申込み及び試験案内（郵送申込み用）の配布<br>
			試験案内配布期間　令和5年7月3日(月)から7月31日(月)まで申込み受付期間　　令和5年7月3日(月)から7月31日(月)まで<br>
			※顔写真のサイズはパスポート申請用サイズ（縦4.5cm、横3.5cm、頭頂からあごまでが長さ3.2cm以上3.6cm以下のもの）です。<br>
			顔写真については、指定したサイズ以外など不適切な場合は、差替えを指示することがあります。指示に従わない場合、受験申込書を受付けません。<br>
			※簡易書留郵便として郵便局の窓口で受付されたもので、消印が上記期間中のもののみ受付けます。 それ以外のものは受付けません。</p>
			<p>受験手数料8,200円<br>
			※いったん振り込まれた受験手数料は、申込みが受付されなかった場合及び試験中止の場合を除き、返還しません。<br>
			※受験手数料について　消費税及び地方消費税は非課税です。</p>
			<p>受験申込の受付完了と試験会場の通知<br>
			・受付完了した方へ、試験会場通知(試験会場の所在地及び会場名を記載したはがき）を8月25日までに発送します。<br>
			※「試験会場通知」が8月30日までに届かない場合は、各都道府県の協力機関に必ずお問い合わせください。</p>
			<p>受験票発送日<br>令和5年9月27日(水)<br>
			※「受験票」が10月5日までに届かない場合は、各都道府県の協力機関又は当機構に必ずお問い合わせください。</p>
